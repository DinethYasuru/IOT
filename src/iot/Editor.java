    private void jToggleButton3MouseWheelMoved(java.awt.event.MouseWheelEvent evt) {//GEN-FIRST:event_jToggleButton3MouseWheelMoved
       if (!jToggleButton3.isSelected()) {
            try {
                FileReader reader = new FileReader("src\\config\\configure.properties");
                Properties prop = new Properties();
                prop.load(reader);
                reader.close();
                File file = new File("src\\config\\configure.properties");
                OutputStream out = new FileOutputStream(file);

                this.r3_status = "1";
                this.r3_sd = String.valueOf(JOptionPane.showInputDialog(null, "Enter the Starting Delay in miliseconds", "Relay 3 Programmer", JOptionPane.INFORMATION_MESSAGE));
                this.r3_rt = String.valueOf(JOptionPane.showInputDialog(null, "Enter the Run time in miliseconds", "Relay 3 Programmer", JOptionPane.INFORMATION_MESSAGE));
                prop.setProperty("r3_status", String.valueOf(this.r3_status));
                prop.setProperty("r3_sd", String.valueOf(this.r3_sd));
                prop.setProperty("r3_rt", String.valueOf(this.r3_rt));
                prop.store(out, null);
                out.close();
            } catch (IOException ex) {
                Logger.getLogger(Editor.class.getName()).log(Level.SEVERE, null, ex);
            }
        } else {
            JOptionPane.showMessageDialog(null, "Unable to edit while running", "Runtime Editing Error", JOptionPane.ERROR_MESSAGE);
        }


    }//GEN-LAST:event_jToggleButton3MouseWheelMoved

