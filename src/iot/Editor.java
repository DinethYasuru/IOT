    public void saveData(String key, String value) throws IOException {
        FileReader reader = new FileReader("src\\config\\configure.properties");
        Properties prop = new Properties();
        prop.load(reader);
        reader.close();
        File file = new File("src\\config\\configure.properties");
        OutputStream out = new FileOutputStream(file);

        prop.setProperty(key, value);

        prop.store(out, null);
        out.close();

    }


    private void jToggleButton1MouseWheelMoved(java.awt.event.MouseWheelEvent evt) {//GEN-FIRST:event_jToggleButton1MouseWheelMoved

        if (!jToggleButton1.isSelected()) {
            try {
                FileReader reader = new FileReader("src\\config\\configure.properties");
                Properties prop = new Properties();
                prop.load(reader);
                reader.close();
                File file = new File("src\\config\\configure.properties");
                OutputStream out = new FileOutputStream(file);

                this.r1_status = "1";
                this.r1_sd = String.valueOf(JOptionPane.showInputDialog(null, "Enter the Starting Delay in miliseconds", "Relay 1 Programmer", JOptionPane.INFORMATION_MESSAGE));
                this.r1_rt = String.valueOf(JOptionPane.showInputDialog(null, "Enter the Run time in miliseconds", "Relay 1 Programmer", JOptionPane.INFORMATION_MESSAGE));
                prop.setProperty("r1_status", String.valueOf(this.r1_status));
                prop.setProperty("r1_sd", String.valueOf(this.r1_sd));
                prop.setProperty("r1_rt", String.valueOf(this.r1_rt));
                prop.store(out, null);
                out.close();
            } catch (IOException ex) {
                Logger.getLogger(Editor.class.getName()).log(Level.SEVERE, null, ex);
            }
        } else {
            JOptionPane.showMessageDialog(null, "Unable to edit while running", "Runtime Editing Error", JOptionPane.ERROR_MESSAGE);
        }

    }//GEN-LAST:event_jToggleButton1MouseWheelMoved

